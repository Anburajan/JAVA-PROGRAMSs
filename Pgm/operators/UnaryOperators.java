package operators;

public class UnaryOperators {
    public static void main(String[] args) {
        
        int a = +20;
               
        int b = -20;
        
        System.out.println("a++ = " + a++);

        System.out.println("Value to 'a' post increment = " + a);
        
        System.out.println("++a = " + ++a);     
        
	System.out.println("a-- = " + a--);
        
	System.out.println("Value to 'a' post decrement = " + a);
        
        System.out.println("--a = " + --a);
       	
        
    }
}